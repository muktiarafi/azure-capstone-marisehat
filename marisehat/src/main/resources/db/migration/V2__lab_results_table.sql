CREATE TABLE lab_results (
    id UUID NOT NULL PRIMARY KEY,
    hemogoblin DOUBLE PRECISION NOT NULL DEFAULT 0,
    entrosit DOUBLE PRECISION NOT NULL DEFAULT 0,
    leukosit DOUBLE PRECISION NOT NULL DEFAULT 0,
    trombosit DOUBLE PRECISION NOT NULL DEFAULT 0,
    hematokrit DOUBLE PRECISION NOT NULL DEFAULT 0,
    blob_name VARCHAR(255) NOT NULL NOT NULL DEFAULT 0,
    patient_id UUID NOT NULL REFERENCES patients (id) ON DELETE SET NULL,
    conducted_at DATE NOT NULL DEFAULT  CURRENT_DATE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);